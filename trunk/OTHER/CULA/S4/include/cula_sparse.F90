
! Types
MODULE CULA_SPARSE_TYPE

    IMPLICIT NONE

! Configuration Types

    ! culaIterativeConfig
    TYPE :: CULA_ITERATIVE_CONFIG
      INTEGER :: indexing
      DOUBLE PRECISION :: tolerance
      INTEGER :: maxIterations
      DOUBLE PRECISION :: maxRuntime
      DOUBLE PRECISION, POINTER :: residualVector(:)
      INTEGER :: useInitialResultVector
      INTEGER :: useBestAnswer
      INTEGER :: useStagnationCheck
      INTEGER :: debug
    END TYPE CULA_ITERATIVE_CONFIG

! Result Types

    ! culaIterativeFlag
    INTEGER, PARAMETER :: culaConverged = 0
    INTEGER, PARAMETER :: culaMaxItReached = 1
    INTEGER, PARAMETER :: culaPreconditionerFailed = 2
    INTEGER, PARAMETER :: culaStagnation = 3
    INTEGER, PARAMETER :: culaScalarOutOfRange = 4
    INTEGER, PARAMETER :: culaUnknownIterationError = 5

    ! culaIterativeResidual
    TYPE :: CULA_ITERATIVE_RESIDUAL
      DOUBLE PRECISION :: relative
      DOUBLE PRECISION, POINTER :: byIteration
    END TYPE CULA_ITERATIVE_RESIDUAL

    ! culaIterativeTiming
    TYPE :: CULA_ITERATIVE_TIMING
      DOUBLE PRECISION :: solve
      DOUBLE PRECISION :: preconditioner
      DOUBLE PRECISION :: overhead
      DOUBLE PRECISION :: total
    END TYPE CULA_ITERATIVE_TIMING

    ! culaIterativeResult
    TYPE :: CULA_ITERATIVE_RESULT
      TYPE(CULA_ITERATIVE_CONFIG) :: config
      INTEGER(8) :: code
      INTEGER :: flag
      INTEGER :: iterations
      TYPE(CULA_ITERATIVE_RESIDUAL) :: residual
      TYPE(CULA_ITERATIVE_TIMING) :: timing
    END TYPE CULA_ITERATIVE_RESULT

    ! culaReordering
    INTEGER, PARAMETER :: culaNoReordering = 0
    INTEGER, PARAMETER :: culaAmdReordering = 1
    INTEGER, PARAMETER :: culaSymamdReordering = 2

! Solver Options Structures

    ! culaBicgOptions
    TYPE :: CULA_BICG_OPTIONS
      INTEGER :: reserved
    END TYPE CULA_BICG_OPTIONS

    ! culaBicgstabOptions
    TYPE :: CULA_BICGSTAB_OPTIONS
      INTEGER :: avoidTranspose
    END TYPE CULA_BICGSTAB_OPTIONS

    ! culaBicgstablOptions
    TYPE :: CULA_BICGSTABL_OPTIONS
      INTEGER :: l
    END TYPE CULA_BICGSTABL_OPTIONS

    ! culaCgOptions
    TYPE :: CULA_CG_OPTIONS
      INTEGER :: reserved
    END TYPE CULA_CG_OPTIONS

    ! culaGmresOptions
    TYPE :: CULA_GMRES_OPTIONS
      INTEGER :: restart
    END TYPE CULA_GMRES_OPTIONS
    
    ! culaMinresOptions
    TYPE :: CULA_MINRES_OPTIONS
      INTEGER :: reserved
    END TYPE CULA_MINRES_OPTIONS

! Preconditioner Options Structures

    ! culaEmptyOptions
    TYPE :: CULA_EMPTY_OPTIONS
      INTEGER :: unused
    END TYPE CULA_EMPTY_OPTIONS

    ! culaJacobiOptions
    TYPE :: CULA_JACOBI_OPTIONS
      INTEGER :: reserved
    END TYPE CULA_JACOBI_OPTIONS

    ! culaBlockjacobiOptions
    TYPE :: CULA_BLOCKJACOBI_OPTIONS
      INTEGER :: l
    END TYPE CULA_BLOCKJACOBI_OPTIONS

    ! culaIlu0Options
    TYPE :: CULA_ILU0_OPTIONS
      INTEGER :: reordering
    END TYPE CULA_ILU0_OPTIONS

END MODULE CULA_SPARSE_TYPE

